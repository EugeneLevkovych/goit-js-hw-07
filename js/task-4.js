const loginForm = document.querySelector('.login-form');

loginForm.addEventListener('submit', function (event) {
  // Запобігаємо перезавантаженню сторінки при сабміті
  event.preventDefault();

  // Отримуємо доступ до значень полів форми
  const email = loginForm.elements.email.value.trim();
  const password = loginForm.elements.password.value.trim();

  // Перевіряємо чи всі поля заповнені
  if (!email || !password) {
    alert('All form fields must be filled in');
    return; // Якщо хоча б одне поле не заповнене, зупиняємо виконання
  }

  // Якщо всі поля заповнені, створюємо об'єкт з даними
  const formData = {
    email: email,
    password: password,
  };

  // Виводимо об'єкт з введеними даними в консоль
  console.log(formData);

  // Очищаємо значення полів форми
  loginForm.reset();
});

//Подія submit: Скрипт слухає подію submit на формі. Це дозволяє обробити подію при відправці форми.
// event.preventDefault(): Це необхідно, щоб сторінка не перезавантажувалась після сабміту форми.
// Перевірка заповнення полів: Після того як користувач натискає на кнопку, перевіряється, чи є значення в полях форми (email і password). Якщо хоча б одне поле не заповнене, виводиться попередження через alert.
// Створення об'єкта з даними: Якщо всі поля заповнені, зберігаються значення полів в об'єкті, де ключі — це імена полів форми, а значення — це їх очищені від пробілів значення.
// Очищення форми: Після обробки форми та виведення даних в консоль, форма очищується за допомогою методу reset(), щоб всі поля стали порожніми.
// Тестування:
// Якщо користувач не заповнить одне з полів, спрацює alert, і форма не буде відправлена.
// Якщо всі поля заповнені, дані виводяться в консоль, і форма очищується.
